package com.bridgelabz.oops.DeckOfCards;

public class Card {
	
	private String suit;
	private String rank;
	Card(){
		
	}
	public String getSuit() {
		return suit;
	}
	public void setSuit(String suit) {
		this.suit = suit;
	}
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	

}
